export class BlogPost {
  /**
   * The unique identifier of the blog post
   */
  id: number;

  /**
   * The title of the blog post
   */
  title: string;

  /**
   * The content of the blog post
   */
  content: string;

  /**
   * The date the blog post was created
   */
  createdAt: Date;

  /**
   * The date the blog post was last updated
   */
  updatedAt: Date;
}
